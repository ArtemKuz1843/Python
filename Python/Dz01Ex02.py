# Задача 4: Петя, Катя и Сережа делают из бумаги журавликов. Вместе они сделали S журавликов.
# Сколько журавликов сделал каждый ребенок, если известно, что Петя и Сережа сделали одинаковое количество журавликов,
# а Катя сделала в два раза больше журавликов, чем Петя и Сережа вместе?
# 6 -> 1  4  1
# 24 -> 4  16  4
# 60 -> 10  40  10

paperСranes = int(input('Введите количество журавликов: '))

x = paperСranes // 6
paperСranesPetja = x
paperСranesSereja = x
paperСranesKatja = 4 * x

print(f'Петя сделал {paperСranesPetja} журавлик(а,ов),')
print(f'Серёжа - {paperСranesSereja} журавлик(а,ов),')
print(f'Катя - {paperСranesKatja} журавлик(а,ов)')